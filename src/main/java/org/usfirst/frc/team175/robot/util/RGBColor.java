package org.usfirst.frc.team175.robot.util;

/**
 * @author Arvind
 */
public class RGBColor {

    private int mRed;
    private int mGreen;
    private int mBlue;

    public RGBColor(int red, int green, int blue) {
        mRed = red;
        mGreen = green;
        mBlue = blue;
    }

    public int getRed() {
        return mRed;
    }

    public int getGreen() {
        return mGreen;
    }

    public int getBlue() {
        return mBlue;
    }

}
